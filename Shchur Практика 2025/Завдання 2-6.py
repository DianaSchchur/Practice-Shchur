def number_to_words(n: int) -> str:
    ones = ["нуль", "один", "два", "три", "чотири", "п'ять", "шість", "сім", "вісім", "дев'ять"]
    teens = ["десять", "одинадцять", "дванадцять", "тринадцять", "чотирнадцять", "п'ятнадцять", 
             "шістнадцять", "сімнадцять", "вісімнадцять", "дев'ятнадцять"]
    tens = ["", "десять", "двадцять", "тридцять", "сорок", "п'ятдесят", 
            "шістдесят", "сімдесят", "вісімдесят", "дев'яносто"]
    hundreds = ["", "сто", "двісті", "триста", "чотириста", "п'ятсот", 
                "шістсот", "сімсот", "вісімсот", "дев'ятсот"]
    if n == 1000:
        return "тисяча"

    result = []  # Список для збору слів

    # Розбір сотень
    if n >= 100:
        result.append(hundreds[n // 100])  # Додаємо слово для сотень
        n %= 100  # Залишок після видалення сотень

    # Розбір десятків і одиниць
    if 10 <= n <= 19:
        result.append(teens[n - 10])  # Для чисел від 10 до 19 використовуємо список teens
    else:
        if n >= 20:
            result.append(tens[n // 10])  # Додаємо десятки
        if n % 10 != 0:
            result.append(ones[n % 10])  # Додаємо одиниці

    return " ".join(filter(None, result))  # Повертаємо рядок без порожніх значень

# Приклад використання
number = int(input("Введіть число від 0 до 1000: "))
if 0 <= number <= 1000:
    print(f"Число словами: {number_to_words(number)}")
else:
    print("Будь ласка, введіть число в допустимому діапазоні.")
